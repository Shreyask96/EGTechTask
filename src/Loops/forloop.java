package Loops;

public class forloop {

public static void main(String[] args) {
		

		for(int i=0;i<10;i++)
		{
			System.out.println("hi");
		}
		
		for(int i=20;i<40;i++) 
		{
			System.out.println(i);
		}

		
		for (int i = 10; i >=0; i--) {               //printing in reverse order
			
			System.out.println(i);
		}
		
	}
}
