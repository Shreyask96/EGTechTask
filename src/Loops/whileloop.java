package Loops;

public class whileloop {
	public static void main(String[] args) {

		int i = 0;
		while (i < 10) {
			System.out.println(i);
			i++;
		}

		
		int j=11; 	
		while(j<10) 
		{	
			System.out.println("hi");
			j++;
		}
	
		int k=0; 	
		while(k<=10) 
		{	
			System.out.println(k);
	        k++;
		}
		
		int l=10; 	
		while(l>0) 
		{	
			System.out.println("hi");
			l--;
		}
		
		int a=10; 	
		while(a>0);  //reverse order dcrement
		{	
			System.out.println(a); 
			a--; 
		}
	}

}
